package br.com.projectSpringForTest.model;

public class BlockedPropose {
    private Integer poliza;
    private Integer aplica;
    private int apendice;
    private Integer fecha;
    private Integer agente;
    private int operativa;
    private int ramo;

    public Integer getPoliza() {
        return poliza;
    }

    public void setPoliza(Integer poliza) {
        this.poliza = poliza;
    }

    public Integer getAplica() {
        return aplica;
    }

    public void setAplica(Integer aplica) {
        this.aplica = aplica;
    }

    public int getApendice() {
        return apendice;
    }

    public void setApendice(int apendice) {
        this.apendice = apendice;
    }

    public Integer getFecha() {
        return fecha;
    }

    public void setFecha(Integer fecha) {
        this.fecha = fecha;
    }

    public Integer getAgente() {
        return agente;
    }

    public void setAgente(Integer agente) {
        this.agente = agente;
    }

    public int getOperativa() {
        return operativa;
    }

    public void setOperativa(int operativa) {
        this.operativa = operativa;
    }

    public int getRamo() {
        return ramo;
    }

    public void setRamo(int ramo) {
        this.ramo = ramo;
    }
}
