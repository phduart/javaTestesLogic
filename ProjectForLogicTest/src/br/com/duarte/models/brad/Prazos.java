package br.com.duarte.models.brad;

public class Prazos {
    int idPlano;
    int prazoInicio;
    int prazoFim;

    public int getIdPlano() {
        return idPlano;
    }

    public void setIdPlano(int idPlano) {
        this.idPlano = idPlano;
    }

    public int getPrazoInicio() {
        return prazoInicio;
    }

    public void setPrazoInicio(int prazoInicio) {
        this.prazoInicio = prazoInicio;
    }

    public int getPrazoFim() {
        return prazoFim;
    }

    public void setPrazoFim(int prazoFim) {
        this.prazoFim = prazoFim;
    }
}
